#include<stdio.h>

int main() {
	// first way
	int one;
	int two;
	one = 2;
	two = 3;
	printf("The first value = %d\n", one);
	printf("The second value = %d\n", two);
	// second way
	int three, four;
	three = 4, four = 5;
	printf("The third value is %d and the fourth value is %d\n", three, four);
	// third way
	int five = 6, six = 7;
	printf("The fifth value is %d and the sixth value is %d\n", five, six);
}